package controller;

/**
 * The FileFormatEnum represents the supported file formats for images.
 */
enum FileFormatEnum {
  /**
   * Portable Network Graphics (PNG) file format.
   */
  png,

  /**
   * Joint Photographic Experts Group (JPEG) file format.
   */
  jpg,

  /**
   * Portable Pixmap (PPM) file format.
   */
  ppm
}

