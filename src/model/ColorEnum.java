package model;

/**
 * The ColorEnum represents predefined color components such as RED, GREEN, and BLUE.
 * It is commonly used in RGB image processing to specify color channels.
 */
public enum ColorEnum {
  /**
   * The RED color component.
   */
  RED,
  /**
   * The GREEN color component.
   */
  GREEN,
  /**
   * The BLUE color component.
   */
  BLUE
}
